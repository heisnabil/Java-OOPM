public class Loops {
    public static void main(String[] args) {
        for (int i = 1; i <= 3; i++) System.out.println("For: " + i);
        int j = 1;
        while (j <= 3) { System.out.println("While: " + j); j++; }
        int k = 1;
        do { System.out.println("Do-While: " + k); k++; } while (k <= 3);
    }
}
